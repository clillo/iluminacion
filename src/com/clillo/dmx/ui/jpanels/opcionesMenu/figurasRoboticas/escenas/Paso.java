package com.clillo.dmx.ui.jpanels.opcionesMenu.figurasRoboticas.escenas;

public class Paso {

    private String nombre;
    private int dimmer;
    private int speed;
    private int posicion;
    private int encendido;
    private long reloj;
    
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getDimmer() {
		return dimmer;
	}
	public void setDimmer(int dimmer) {
		this.dimmer = dimmer;
	}
	public int getSpeed() {
		return speed;
	}
	public void setSpeed(int speed) {
		this.speed = speed;
	}
	public int getPosicion() {
		return posicion;
	}
	public void setPosicion(int posicion) {
		this.posicion = posicion;
	}
	public int getEncendido() {
		return encendido;
	}
	public void setEncendido(int encendido) {
		this.encendido = encendido;
	}
	public long getReloj() {
		return reloj;
	}
	public void setReloj(long reloj) {
		this.reloj = reloj;
	}
    
}
